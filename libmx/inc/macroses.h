#ifndef MACROSES_H
#define MACROSES_H

#define MX_SUM(x, y) ((x) + (y))
#define MX_SUB(x, y) ((x) - (y))
#define MX_MUL(x, y) ((x) * (y))
#define MX_DIV(x, y) ((x) / (y))

#define MX_MIN(x, y) ((x) < (y) ? (x) : (y))
#define MX_MAX(x, y) ((x) > (y) ? (x) : (y))

#define MX_ABS(x) ((x) < 0 ? -(x) : (x))

#endif
